package util // このファイルが属するパッケージの名前 (ディレクトリ名と同じにするのがGoの慣例)

// IsLeapYear は与えられた年が閏年かどうかを判定します。
// 閏年の条件は以下の通りです:
// 1. 400で割り切れる年は閏年である。
// 2. 4で割り切れるが100で割り切れない年は閏年である。
// それ以外は閏年ではない。
func IsLeapYear(year int) bool { // func: 関数定義, IsLeapYear: 関数名 (大文字でPublic), year int: 引数 (名前 型), bool: 戻り値の型
	// 無効な年 (例: 0以下) に対する特定の挙動が必要な場合、ここにロジックを追加できます。
	// 今回のPythonロジックに合わせるため、ここでは純粋な判定のみを行います。
	if year <= 0 {
		return false // 例: 0以下の年は閏年ではないと仮定
	}
	return (year%400 == 0) || (year%4 == 0 && year%100 != 0)
}
